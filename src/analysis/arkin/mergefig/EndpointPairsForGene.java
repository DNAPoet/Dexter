	static Vector<EndpointPairsForGene> getAll()
	{
		Vector<EndpointPairsForGene> ret = new Vector<EndpointPairsForGene>();
		for (String s: INITS)
			ret.add(new EndpointPairsForGene(s));
		return ret;
	}
	
	
